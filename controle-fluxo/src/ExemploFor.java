public class ExemploFor {
    public static void main(String[] args) {
        for (int carneirinhos = 1; carneirinhos <= 20; carneirinhos++) {
            if (carneirinhos == 1)
                System.out.println(carneirinhos + " Carneirinho");
            else
                System.out.println(carneirinhos + " Carneirinhos");
        }
    }

}